package main

import "fmt"

func main() {
	var a = "initial"
	fmt.Println(a)

	var b, c int = 1, 2
	fmt.Println(b, c)

	var d = true
	fmt.Println(d)

	var e int
	fmt.Println(e)

	var s int
	fmt.Println(s)

	var fl float64
	fmt.Println(fl)

	f := "apple"
	fmt.Println(f)
}
